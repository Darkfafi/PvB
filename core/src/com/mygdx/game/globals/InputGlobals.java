package com.mygdx.game.globals;

public final class InputGlobals 
{
	/**
	 * An event type fired by the 'TouchInputHandler' to indicate when a touch has started 
	 */
	public static final String TOUCH_STARTED_EVENT = "TouchStartedEvent";
	/**
	 * An event type fired by the 'TouchInputHandler' to indicate when a touch has been dragged 
	 */
	public static final String TOUCH_DRAGGED_EVENT = "TouchDraggedEvent";
	/**
	 * An event type fired by the 'TouchInputHandler' to indicate when a touch has ended 
	 */
	public static final String TOUCH_ENDED_EVENT = "TouchEndedEvent";
	
	/**
	 * An event type fired by the 'TouchInputHandler' to indicate any touch event
	 */
	public static final String TOUCH_EVENT = "TouchEvent";
}
